def get_bond_logic(bond_name: str) -> dict:
    """查询转债逻辑，返回 dict，未找到返回空 dict"""
    return BOND_LOGIC.get(bond_name, {})


def get_industry_theme(industry_keyword: str) -> dict:
    """模糊匹配行业热点，返回最匹配的一条"""
    for k, v in INDUSTRY_THEMES.items():
        if industry_keyword in k:
            return {k: v}
    return {}


def get_hot_concepts_top(n: int = 5) -> list:
    """返回热度最高的 n 个概念"""
    sorted_items = sorted(HOT_CONCEPTS.items(), key=lambda x: x[1]["热度"], reverse=True)
    return [(k, v) for k, v in sorted_items[:n]]


def get_logic_by_industry(industry: str) -> list:
    """根据行业关键词搜索已有逻辑条目"""
    results = []
    for bond, info in BOND_LOGIC.items():
        if industry in info.get("industry", ""):
            results.append((bond, info))
    return results


